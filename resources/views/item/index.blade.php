<script>
const app = Vue.createApp({
            data() {
                return {
                    data: @json($items),
                    nama : "",
                    harga : 0,
                    alert_success: false,
                    selected_item: {}
                }
            },
            methods: {
                async simpan() {
                    console.log(this.nama, this.harga);
                    const response = await axios.post('/item', {
                        name: this.nama,
                        price: this.harga
                    });

                //if success
                if (response.status == 200) {
                    //get data from /item/getall
                    const response2 = await axios.get('/item/getall');
                    console.log(response2.data);
                    this.data = response2.data;
                    this.alert_success = true;
                    //close modal
                    document.querySelector('[data-modal-hide="static-modal"]').click();
                    this.nama = "";
                    this.harga = 0;
                }

                },
                async show(index) {
                    //alert false
                    this.alert_success = false;
                    //get data from /item/getone
                    const response = await axios.get('/item/getone/' + this.data[index].id);
                    this.selected_item = response.data;
                },
                async simpan_edit() {
                    console.log(this.selected_item);
                    const response = await axios.put('/item/' + this.selected_item.id, {
                        name: this.selected_item.name,
                        price: this.selected_item.price
                    });

                    //if success
                    if (response.status == 200) {
                        //get data from /item/getall
                        const response2 = await axios.get('/item/getall');
                        console.log(response2.data);
                        this.data = response2.data;
                        this.alert_success = true;
                        //close modal
                        document.querySelector('[data-modal-hide="static-modal2"]').click();
                    }
                }
            }
        });
app.mount('#app');
</script>
